    private void txtIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtIngresarActionPerformed
        String elUsr= txtUser.getText();
        String elPwd= txtPwd.getText();
        
        // se crea una peticion de tipo INGRESAR que devolver치 un boolean como respuesta.
        // esto por definici칩n de los servicios provistos por la aplicaci칩n servidora
        
        Peticion peticionIngresar = new Peticion(TipoAccion.INGRESAR, elUsr+"-"+elPwd );
        
        Client conexion = new Client(peticionIngresar);
        
        boolean respuesta = (boolean) conexion.getRespuestaServer();
        lblMensaje.setText(respuesta ? "Todo OK, va para adentro!" 
                                     : "!Credenciales del usuario admin incorrectas!");
        
        
    }//GEN-LAST:event_txtIngresarActionPerformed

